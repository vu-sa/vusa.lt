<template>
  <div class="space-y-6">

    <!-- Hero Card -->
    <Card class="relative overflow-hidden border-0 shadow-sm bg-gradient-to-br from-white to-zinc-50 dark:from-zinc-900 dark:to-zinc-800">
      <CardContent class="p-6">
        <!-- Header Row -->
        <div class="flex items-start justify-between mb-4">
          <div class="space-y-1">
            <div class="flex items-center gap-2 text-sm text-zinc-600 dark:text-zinc-400">
              <component :is="getInstitutionIcon()" class="h-4 w-4" />
              <span>{{ typeof mainInstitution === 'string' ? mainInstitution : mainInstitution.name }}</span>
            </div>
            
            <h1 class="text-2xl md:text-3xl font-bold text-zinc-900 dark:text-zinc-100 leading-tight">
              {{ meetingTitle }}
            </h1>
          </div>
          
          <!-- Actions -->
          <DropdownMenu>
            <DropdownMenuTrigger asChild>
              <Button variant="outline" size="icon" class="h-10 w-10">
                <MoreHorizontal class="h-4 w-4" />
              </Button>
            </DropdownMenuTrigger>
            <DropdownMenuContent align="end">
              <DropdownMenuItem @click="$emit('edit')">
                <Edit class="h-4 w-4 mr-2" />
                {{ $t('Redaguoti posėdį') }}
              </DropdownMenuItem>
              <DropdownMenuSeparator />
              <DropdownMenuItem 
                @click="$emit('showDeleteDialog')" 
                class="text-destructive focus:text-destructive"
              >
                <Trash2 class="h-4 w-4 mr-2" />
                {{ $t('Šalinti posėdį') }}
              </DropdownMenuItem>
            </DropdownMenuContent>
          </DropdownMenu>
        </div>
        
        <!-- Meeting Info Row -->
        <div class="flex flex-col md:flex-row md:items-center gap-4 mb-4">
          <!-- Date and Time -->
          <div class="flex items-center gap-4 text-sm">
            <div class="flex items-center gap-2 text-zinc-700 dark:text-zinc-300">
              <Calendar class="h-4 w-4 text-blue-500" />
              <span class="font-medium">
                {{ formatStaticTime(new Date(meeting.start_time), {
                  year: "numeric",
                  month: "long", 
                  day: "numeric"
                }) }}
              </span>
            </div>
            
            <div class="flex items-center gap-2 text-zinc-700 dark:text-zinc-300">
              <Clock class="h-4 w-4 text-green-500" />
              <time class="font-medium">
                {{ formatStaticTime(new Date(meeting.start_time), {
                  hour: "2-digit",
                  minute: "2-digit"
                }) }}
              </time>
            </div>
          </div>
          
          <!-- Meeting Types -->
          <div class="flex flex-wrap gap-2" v-if="meeting.types && meeting.types.length > 0">
            <Badge 
              v-for="type in meeting.types" 
              :key="type.id" 
              variant="secondary"
              class="text-xs"
            >
              {{ type.title }}
            </Badge>
          </div>
        </div>
        
        <!-- Progress and Stats -->
        <div class="grid grid-cols-2 md:grid-cols-4 gap-4 pt-4 border-t border-zinc-200 dark:border-zinc-700">
          <!-- Agenda Items Count -->
          <div class="text-center">
            <div class="text-lg font-bold text-zinc-900 dark:text-zinc-100">
              {{ agendaItemsCount }}
            </div>
            <div class="text-xs text-zinc-600 dark:text-zinc-400">
              {{ $t('Darbotvarkės punktai') }}
            </div>
          </div>
          
          <!-- Completed Items -->
          <div class="text-center">
            <div class="text-lg font-bold text-green-600 dark:text-green-400">
              {{ completedItemsCount }}
            </div>
            <div class="text-xs text-zinc-600 dark:text-zinc-400">
              {{ $t('Išnagrinėta') }}
            </div>
          </div>
          
          <!-- Files Count -->
          <div class="text-center" v-if="meeting.files && meeting.files.length > 0">
            <div class="text-lg font-bold text-blue-600 dark:text-blue-400">
              {{ meeting.files.length }}
            </div>
            <div class="text-xs text-zinc-600 dark:text-zinc-400">
              {{ $t('Failai') }}
            </div>
          </div>
          
          <!-- Tasks Count -->
          <div class="text-center" v-if="meeting.tasks && meeting.tasks.length > 0">
            <div class="text-lg font-bold text-orange-600 dark:text-orange-400">
              {{ meeting.tasks.length }}
            </div>
            <div class="text-xs text-zinc-600 dark:text-zinc-400">
              {{ $t('Užduotys') }}
            </div>
          </div>
        </div>
        
        <!-- Progress Bar -->
        <div v-if="agendaItemsCount > 0" class="mt-4">
          <div class="flex items-center justify-between text-xs text-zinc-600 dark:text-zinc-400 mb-1">
            <span>{{ $t('Posėdžio progresas') }}</span>
            <span>{{ Math.round(progressPercentage) }}%</span>
          </div>
          <div class="w-full bg-zinc-200 dark:bg-zinc-700 rounded-full h-2">
            <div 
              class="bg-gradient-to-r from-blue-500 to-green-500 h-2 rounded-full transition-all duration-300"
              :style="{ width: `${progressPercentage}%` }"
            ></div>
          </div>
        </div>
      </CardContent>
    </Card>
  </div>
</template>

<script setup lang="ts">
import { computed } from 'vue'
import { trans as $t } from 'laravel-vue-i18n'
import { formatStaticTime } from '@/Utils/IntlTime'
import { genitivizeEveryWord } from '@/Utils/String'

// UI Components
import { Card, CardContent } from '@/Components/ui/card'
import { Button } from '@/Components/ui/button'
import { Badge } from '@/Components/ui/badge'
import { 
  DropdownMenu, 
  DropdownMenuContent, 
  DropdownMenuItem, 
  DropdownMenuSeparator, 
  DropdownMenuTrigger 
} from '@/Components/ui/dropdown-menu'

// Icons
import { 
  Building, 
  Calendar, 
  Clock, 
  MoreHorizontal, 
  Edit, 
  Trash2, 
  GraduationCap, 
  Users 
} from 'lucide-vue-next'

interface Props {
  meeting: App.Entities.Meeting
  mainInstitution: App.Entities.Institution | string
  agendaItems: App.Entities.AgendaItem[]
}

interface Emits {
  (e: 'edit'): void
  (e: 'showDeleteDialog'): void
}

const props = defineProps<Props>()
defineEmits<Emits>()

const meetingTitle = computed(() => {
  if (props.meeting.title && props.meeting.title !== "") {
    return props.meeting.title
  }
  
  const institutionName = typeof props.mainInstitution === 'string' 
    ? props.mainInstitution 
    : props.mainInstitution.name
    
  return `${formatStaticTime(new Date(props.meeting.start_time), {
    year: "numeric",
    month: "long",
    day: "numeric",
  })} ${genitivizeEveryWord(institutionName)} posėdis`
})

const agendaItemsCount = computed(() => props.agendaItems.length)

const completedItemsCount = computed(() => {
  return props.agendaItems.filter(item => 
    item.decision === 'positive' || 
    item.decision === 'negative' || 
    item.decision === 'neutral'
  ).length
})

const progressPercentage = computed(() => {
  if (agendaItemsCount.value === 0) return 0
  return (completedItemsCount.value / agendaItemsCount.value) * 100
})

const getInstitutionIcon = () => {
  if (typeof props.mainInstitution === 'string') {
    return Building
  }
  
  // You can add logic here to return different icons based on institution type
  const institutionName = props.mainInstitution.name?.toLowerCase() || ''
  
  if (institutionName.includes('fakultet') || institutionName.includes('institu')) {
    return GraduationCap
  }
  
  if (institutionName.includes('studen') || institutionName.includes('atstov')) {
    return Users
  }
  
  return Building
}
</script>